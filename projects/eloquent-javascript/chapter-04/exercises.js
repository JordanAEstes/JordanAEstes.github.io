function sum(sumArray) {
//use for loop to iterate through array and add the values stored within
var finalSum = 0;
if (sumArray.length === 0){return finalSum}

for (var i = 0; i < sumArray.length; i++){
  finalSum += sumArray[i]
}
return finalSum;
}

