////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1) {
  //using a for loop, add all numbers from start to end to an array
  var rangeArray = [];
  if (step < 0){
    return rangeArray;
  }
  if(start === end){ return rangeArray}
  if(start < end){
    for (var i = start; i <= end; i += step){
     rangeArray.push(i); 
    }
  } else {
    for (var i = start; i >= end; i -= step){
        rangeArray.push(i);
    }
  }
  return rangeArray;
}



