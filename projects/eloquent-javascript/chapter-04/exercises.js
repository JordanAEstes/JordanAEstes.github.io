// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(arrayToRev) {
// use for loop to iterate over an array and create a new array in reversed order
  var reversedArray = [];
  for (var i = arrayToRev.length - 1; i >= 0; i--){
    reversedArray.push(arrayToRev[i]);
  }
  return reversedArray;
}

